package ClassExample01;

public class Calc01 {
	int i = 0, j = 0;

	public int add(int x, int y) {
		return x + y;
	}

	public int divide() {
		return i / j;
	}

	public void sub(int x, int y) {
		System.out.println(x - y);
	}

	public void mul() {
		System.out.println(i * j);
	}
}
